CreateRespawnNeutralTimer = function(B)
    local v
    return function()
        local C, D, E, F, G, H, I, J, K, L, M, N, O, P, Q, R
        D = 0
        NeutralMinionCamps[B].DamageBonus = NeutralMinionCamps[B].DamageBonus + 0
        NeutralMinionCamps[B].HealthBonus = NeutralMinionCamps[B].HealthBonus + 0
        for I = 1, #NeutralMinionCamps[B].Groups, 1 do
            if math.random(100) <= NeutralMinionCamps[B].GroupsChance[I] + D and false == false then
                NeutralMinionCamps[B].AliveTracker = {}
                for M = 1, #NeutralMinionCamps[B].Groups[I], 1 do
                    SpawnNeutralMinion(NeutralMinionCamps[B], B, I, M)
                    NeutralMinionCamps[B].AliveTracker[M] = true
                end
            end
        end
    end
end
InitializeNeutralMinionInfo = function()
    local u, v, w, x, y, z, A, S, T, U, V, W, X, Y, Z, _, a0, a1, a2
    for x = 1, #NeutralMinionCamps, 1 do
        for S = 1, #NeutralMinionCamps[x].Groups, 1 do
            NeutralMinionCamps[x].UniqueNames[S] = {}
            for W = 1, #NeutralMinionCamps[x].Groups[S], 1 do
                NeutralMinionCamps[x].UniqueNames[S][W] =
                    NeutralMinionCamps[x].Groups[S][W] .. x .. "." .. S .. "." .. W
            end
        end
        y = #NeutralTimers
        NeutralTimers[y + 1] = CreateRespawnNeutralTimer(x)
        NeutralMinionCamps[x].Timer = NeutralTimers[y + 1]
        CreateNeutralCamp(NeutralMinionCamps[x], x)
    end
end
SpawnInitialNeutralMinions = function(B)
    local v, w, x, y, z, A, S, T, U, V, W, X
    if B <= #NeutralMinionCamps then
        w = 0
        x = false
        for S = 1, #NeutralMinionCamps[B].Groups, 1 do
            if 0 < NeutralMinionCamps[B].GroupDelaySpawnTime and x == false then
                NeutralMinionCamps[B].AliveTracker = {}
                for W = 1, #NeutralMinionCamps[B].Groups[S], 1 do
                    NeutralMinionCamps[B].AliveTracker[W] = true
                end
            else
                if math.random(100) <= NeutralMinionCamps[B].GroupsChance[S] + w and x == false then
                    NeutralMinionCamps[B].AliveTracker = {}
                    for W = 1, #NeutralMinionCamps[B].Groups[S], 1 do
                        NeutralMinionCamps[B].AliveTracker[W] = true
                    end
                end
            end
        end
    end
end
NeutralMinionDeath = function(B, a3, a4)
    local x, y, z, A, S, T, U, V, W, X, Y, Z, _, a0, a1
    y = 0
    for T = 1, #NeutralMinionCamps, 1 do
        for X = 1, #NeutralMinionCamps[T].UniqueNames, 1 do
            for a0 = 1, #NeutralMinionCamps[T].UniqueNames[X], 1 do
                if NeutralMinionCamps[T].UniqueNames[X][a0] == B then
                    y = T
                    NeutralMinionCamps[T].AliveTracker[a0] = false
                end
            end
        end
    end
    if 0 < y then
        for T = 1, #NeutralMinionCamps[y].AliveTracker, 1 do
            if NeutralMinionCamps[y].AliveTracker[T] == true then
                x = false
            end
        end
        if x != false then
            InitNeutralMinionTimer(
                NeutralMinionCamps[y].Timer,
                NeutralMinionCamps[y].TimerType,
                NeutralMinionCamps[y].GroupsRespawnTime,
                0,
                false
            )
        end
    end
end
