CreateRespawnNeutralTimer = function(C)
    local w
    return function()
        local D, E, F, G, H, I, J, K, L, M, N, O, P, Q, R, S
        D = math.random(100)
        E = 0
        F = false
        NeutralMinionCamps[C].DamageBonus = NeutralMinionCamps[C].DamageBonus + 0
        NeutralMinionCamps[C].HealthBonus = NeutralMinionCamps[C].HealthBonus + 0
        if NeutralMinionCamps[C].MaxRespawn == -1 then
            for J = 1, #NeutralMinionCamps[C].Groups, 1 do
                if D <= NeutralMinionCamps[C].GroupsChance[J] + E and F == false then
                    NeutralMinionCamps[C].AliveTracker = {}
                    for N = 1, #NeutralMinionCamps[C].Groups[J], 1 do
                        SpawnNeutralMinion(NeutralMinionCamps[C], C, J, N)
                        NeutralMinionCamps[C].AliveTracker[N] = true
                    end
                end
            end
        else
            if NeutralMinionCamps[C].MaxRespawn > NeutralMinionCamps[C].SpawnCount then
                for J = 1, #NeutralMinionCamps[C].Groups, 1 do
                    if D <= NeutralMinionCamps[C].GroupsChance[J] + E and F == false then
                        NeutralMinionCamps[C].AliveTracker = {}
                        for N = 1, #NeutralMinionCamps[C].Groups[J], 1 do
                            SpawnNeutralMinion(NeutralMinionCamps[C], C, J, N)
                            NeutralMinionCamps[C].AliveTracker[N] = true
                        end
                    end
                end
                NeutralMinionCamps[C].SpawnCount = NeutralMinionCamps[C].SpawnCount + 1
            end
        end
    end
end
